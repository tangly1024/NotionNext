// 获取 API 内容并替换 HEO_NOTICE_BAR 的内容 (请注意风险!)
function fetchAndReplaceNoticeBar() {
  const apiUrl = 'https://api.vvhan.com/api/text/sexy';
  const proxyUrl = `https://cors-anywhere.herokuapp.com/${apiUrl}`;

  fetch(proxyUrl)
    .then(response => {
      if (!response.ok) {
        throw new Error(`HTTP error! status: ${response.status}`);
      }
      return response.text();
    })
    .then(data => {
      //  找到要替换的项目
         const apiNoticeItem = CONFIG.HEO_NOTICE_BAR.find(item => item.title === 'API_CONTENT')
        // 替换占位符的内容
        if(apiNoticeItem){
          apiNoticeItem.title = data
        }
      console.log("修改后的 HEO_NOTICE_BAR:", CONFIG.HEO_NOTICE_BAR); // 可选：输出修改后的 HEO_NOTICE_BAR 查看
    })
    .catch(error => {
      console.error("获取 API 数据时发生错误:", error);
    });
}


fetchAndReplaceNoticeBar(); // 调用函数来获取API内容并替换

